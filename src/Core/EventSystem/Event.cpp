/*============================================================================*/
#include "Event.hpp"
/*============================================================================*/
using namespace Sh;
/*============================================================================*/

Event::Event()
    : received(false)
    { }

/*----------------------------------------------------------------------------*/

bool Event::isReceived() const {
    return received;
}

/*----------------------------------------------------------------------------*/

bool Event::happen(Listener* listener) {
    return listener->onEvent(*this);
}

/*----------------------------------------------------------------------------*/

EventMask Event::mask() {
    return NONE;
}

